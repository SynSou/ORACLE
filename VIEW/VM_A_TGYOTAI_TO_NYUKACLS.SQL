CREATE OR REPLACE FORCE VIEW VM_A_TGYOTAI_TO_NYUKACLS
    (DLV_CD,TGYOTAI,NYUKA_CLS)
AS
SELECT
---DISTINCT -----追加なら、ビューが相当遅い、各取得でDISTINCT処理
 DLV_CD
,TGYOTAI
,CASE 
--2015/3/1 s.ohshima　北根さんからの要望対応
WHEN TGYOTAI=' ' THEN '02'
WHEN TGYOTAI='0' THEN '02'
--2015/3/1 s.ohshima　北根さんからの要望対応
WHEN TGYOTAI='1' THEN '02' 
WHEN TGYOTAI='2' THEN '02' 
WHEN TGYOTAI='3' THEN '04' 
WHEN TGYOTAI='4' THEN '02' 
WHEN TGYOTAI='5' THEN '03' 
WHEN TGYOTAI='6' THEN '03' 
WHEN TGYOTAI='7' THEN '03' 
ELSE NULL
END NYUKA_CLS
FROM TM_DELIVERY
/
